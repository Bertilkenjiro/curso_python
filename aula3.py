"""
Python = Lingaguem de programacao
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas

"""

# Aspas simples
print('Luiz Otávio')
print(1, 'Luiz "Otávio"')

# Aspas duplas
print("Luiz Otávio")
print(2, "Luiz 'Otávio'")

# Escape (para aspas dentro de aspas)
print("Luiz \"Otávio\"")

# r
print(r"Luiz \"Otávio\"")
